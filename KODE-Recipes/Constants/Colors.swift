import Foundation
import UIKit

extension UIColor {
    struct BaseTheme {
        static var background: UIColor? { return UIColor(named: "BackgroundColor") }
        static var tableBackground: UIColor? { return UIColor(named: "TableBackgroundColor") }
        static var cellBackground: UIColor? { return UIColor(named: "CellBackgroundColor") }
        static var pageControlMain: UIColor? { return UIColor(named: "PageControlColorMain") }
    }
    
}
